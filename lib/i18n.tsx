export type TKey = keyof (typeof dict)["lo"];

const I18nCtx = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: TKey) => string }>({
  lang: "lo",
  setLang: () => {},
  t: (k) => k,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("lo");
  useEffect(() => {
    const saved = localStorage.getItem("gs-lang") as Lang | null;
    if (saved === "lo" || saved === "th") setLangState(saved);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("gs-lang", l);
  };
  const t = (k: TKey) => dict[lang][k] ?? dict.lo[k] ?? k;
  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

export function useI18n() {
  return useContext(I18nCtx);
}
